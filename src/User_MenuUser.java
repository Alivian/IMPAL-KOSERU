    public User_MenuUser(Person P) {
        initComponents();
        this.setLocationRelativeTo(null);
        U_txKd_Anggota.setText(P.getKode_angg());
        U_txNama.setText(P.getNama());
        U_txProfesi.setText(P.getProfesi());
        U_txEmail.setText(P.getEmail());
        U_txTTL.setText(P.getTtl());
        totSaldo=pprn.cekSaldo(P.getKode_angg());
        this.mdl = (DefaultTableModel) U_tblPinjam.getModel();
        this.mdlp = (DefaultTableModel) U_tbPelunasan.getModel();
        for (Pinjam p : P.getPinjam()){
            mdl.addRow(new Object[]{p.getTgl(),p.getKetPinjam(),p.getUang(), p.getStatus(),p.getPelunasan()});
            if(p.getTgl_lunas()!=null){
                mdlp.addRow(new Object[]{p.getTgl_lunas(),p.getUang()});
            }
        }
        this.mdll = (DefaultTableModel) U_tbSimpan.getModel();
        
        for (Simpan s : P.getSimpan()){
            mdll.addRow(new Object[]{s.getTgl(),s.getUang()});
        }
        this.mdltrk = (DefaultTableModel) U_tbPenarikan.getModel();
        mdltrk.setRowCount(0);
        pprn.getPenarikanUser(mdltrk, P.getKode_angg());
        this.baru = new User_ReqPinjam(U_txKd_Anggota.getText(), mdl);
    }
