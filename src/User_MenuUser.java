    private void U_btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_U_btnEditActionPerformed
        if (edit == 0 ){
            U_txNama.setEditable(true);
            U_txEmail.setEditable(true);
            U_txProfesi.setEditable(true);
            U_btnLogout.setVisible(false);
            U_btnReqPinjam.setVisible(false);
            U_btnEdit.setText("Konfirmasi");
            edit=1;
        }else{
            if(U_txNama.getText().length()==0||U_txProfesi.getText().length()==0||U_txEmail.getText().length()==0){
                JOptionPane.showMessageDialog(rootPane, "Data tidak boleh kosong","GAGAL",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                DatabaseUser a= new DatabaseUser();
                U_txNama.setEditable(false);
                U_txEmail.setEditable(false);
                U_txProfesi.setEditable(false);
                U_btnLogout.setVisible(true);
                U_btnReqPinjam.setVisible(true);
                if(a.update(U_txKd_Anggota.getText(), U_txNama.getText(),U_txProfesi.getText(),U_txEmail.getText())!=0){
                    JOptionPane.showMessageDialog(rootPane, "Akun baru telah terupdate","BERHASIL",JOptionPane.INFORMATION_MESSAGE);
                    edit=0;
                    U_btnEdit.setText("Edit Profile");
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Terjadi Kesalahan Sistem","GAGAL",JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_U_btnEditActionPerformed

